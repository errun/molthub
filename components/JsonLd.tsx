import type { ReactNode } from "react";

type JsonLdProps = {
  id: string;
  data: Record<string, unknown>;
};

export default function JsonLd({ id, data }: JsonLdProps): ReactNode {
  return (
    <script
      id={id}
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: JSON.stringify(data) }}
    />
  );
}
